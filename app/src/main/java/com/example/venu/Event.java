package com.example.venu;

public class Event {

    private String title;
    private String venue;
    private String date;
    private String price;

    public String getTitle() {
        return title;
    }

    public String getVenue() {
        return venue;
    }

    public String getDate() {
        return date;
    }

    public String getPrice() {
        return price;
    }


}
